// Function to fetch polyline from Google Maps Directions API
async function getPolyline(start, end, via) {
  try {
    const response = await gmaps.directions({
      params: {
        origin: start,
        destination: end,
        waypoints: via.length ? via : undefined,
        mode: "driving",
        region: "ph",
        key: GOOGLE_MAPS_API_KEY,
      },
    });

    if (response.data.routes.length > 0) {
      const polyline = response.data.routes[0].overview_polyline;
      console.log(`Fetched polyline for route from ${start} to ${end}`);
      return polyline;
    } else {
      console.warn(`No routes found from ${start} to ${end}`);
      return null;
    }
  } catch (error) {
    console.error(`Error fetching polyline from ${start} to ${end}:`, error);
    return null;
  }
}

// Function to upload routes to Firestore with polylines
async function uploadRoutes() {
  try {
    for (const route of routes) {
      // Fetch polyline
      const polyline = await getPolyline(route.start, route.end, route.via);

      // Add polyline to route object
      const routeWithPolyline = {
        ...route,
        path: polyline || "", // Use empty string if polyline fetch fails
      };

      // Upload to routes collection
      await setDoc(doc(db, "routes", route.name), routeWithPolyline);
      console.log(
        `Successfully uploaded route: ${route.name} with polyline: ${
          polyline ? "Yes" : "No"
        }`
      );
    }
    console.log("All routes uploaded successfully!");
  } catch (error) {
    console.error("Error uploading routes:", error);
  }
}

